public class PerOptions {
    private int ivRes;
    private String title;

    public PerOptions(int ivRes, String title) {
        this.ivRes = ivRes;
        this.title = title;
    }

    public int getIvRes() {
        return ivRes;
    }

    public void setIvRes(int ivRes) {
        this.ivRes = ivRes;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
